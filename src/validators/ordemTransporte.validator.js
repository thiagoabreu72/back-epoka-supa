const Joi = require("joi");

module.exports = {
  create: Joi.object({
    situacao_id: Joi.number().integer().positive().required(),
    lote: Joi.string().max(50).allow("", null),
    produto: Joi.string().max(255).required(),
    tipo_carga_id: Joi.number().integer().positive().required(),
    embarcador_id: Joi.number().integer().positive(),
    origem: Joi.string().max(255).required(),
    destino: Joi.string().max(255).required(),
    cliente_pagador: Joi.string().max(255).allow("", null),
    placa: Joi.string().max(20).allow("", null),
    ctrc: Joi.string().max(50).allow("", null),
    valor_frete: Joi.number().positive().allow(null),
    telefone_motorista: Joi.string().max(20).allow("", null),
    frete_motorista: Joi.number().positive().allow(null),
    tag_pedagio_id: Joi.number().integer().positive().allow(null),
    data_carregamento: Joi.date().iso().allow(null),
    observacoes: Joi.string().allow("", null),
    rota: Joi.string().allow("", null),
    manifestador_id: Joi.number().integer().positive().allow(null),
  }),

  update: Joi.object({
    situacao_id: Joi.number().integer().positive(),
    lote: Joi.string().max(50).allow("", null),
    produto: Joi.string().max(255),
    tipo_carga_id: Joi.number().integer().positive(),
    origem: Joi.string().max(255),
    destino: Joi.string().max(255),
    cliente_pagador: Joi.string().max(255).allow("", null),
    placa: Joi.string().max(20).allow("", null),
    ctrc: Joi.string().max(50).allow("", null),
    valor_frete: Joi.number().positive().allow(null),
    telefone_motorista: Joi.string().max(20).allow("", null),
    frete_motorista: Joi.number().positive().allow(null),
    tag_pedagio_id: Joi.number().integer().positive().allow(null),
    data_carregamento: Joi.date().iso().allow(null),
    observacoes: Joi.string().allow("", null),
    rota: Joi.string().allow("", null),
    manifestador_id: Joi.number().integer().positive().allow(null),
  }).min(1),

  filter: Joi.object({
    situacao_id: Joi.number().integer().positive(),
    embarcador_id: Joi.number().integer().positive(),
    manifestador_id: Joi.number().integer().positive(),
    tipo_carga_id: Joi.number().integer().positive(),
    data_inicio: Joi.date().iso(),
    data_fim: Joi.date().iso().min(Joi.ref("data_inicio")),
    placa: Joi.string().max(20),
    origem: Joi.string().max(255),
    destino: Joi.string().max(255),
    cliente_pagador: Joi.string().max(255),
    lote: Joi.string().max(50).allow("", null),
    produto: Joi.string().max(255).allow("", null),
    page: Joi.number().integer().min(1).default(1),
    limit: Joi.number().integer().min(1).max(100).default(10),
  }),
};
